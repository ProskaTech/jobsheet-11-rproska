// Java program to demonstrate
// the Unsigned right shift operator
package shiftoperator;
import java.io.*;

public class unsigned_right_shift {

	public static void main (String[] args)
	{
		byte num1 = 8;
		byte num2 = -8;
	
		System.out.println(num1 >>> 2);
		System.out.println(num2 >>> 2);
	}
}
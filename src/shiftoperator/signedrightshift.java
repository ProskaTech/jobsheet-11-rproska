// Java program to demonstrate
// the Signed right shift operator

package shiftoperator;
import java.io.*;

public class signedrightshift {

	public static void main (String[] args) {
		{		
		int number = 8;
	
		// 2 bit signed right shift
		int Ans = number >> 2;
	
		System.out.println(Ans);
		}
	}

}
